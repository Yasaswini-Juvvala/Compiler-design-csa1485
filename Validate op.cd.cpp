#include <stdio.h>

int main() {
    int x = 10, y = 5;
    
    printf("x + y = %d\n", x + y); 
    printf("x - y = %d\n", x - y); 
    printf("x * y = %d\n", x * y); 
    printf("x / y = %d\n", x / y); 
    printf("x %% y = %d\n", x % y); 
    printf("++x = %d\n", ++x); 
    printf("--y = %d\n", --y); 
    
    return 0;
}
